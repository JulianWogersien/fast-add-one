
#ifndef FAST_ADD_H
#define FAST_ADD_H

inline int fast_add_one(int n) {
    return -~n;
}

inline char fast_add_one(char n) {
    return -~n;
}

inline short int fast_add_one(short int n) {
    return -~n;
}

inline long int fast_add_one(long int n) {
    return -~n;
}

inline long long int fast_add_one(long long int n) {
    return -~n;
}

#endif

